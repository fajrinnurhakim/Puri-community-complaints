			<h1 class="judul_artikel">Penghujan Datang, Bencana Mulai Ancam Banyumas</h1>
			<p class="ket">Di tulis oleh <a href="">Info.purwokerto</a> pada 4 November 2021</p>
			<p class="isi_artikel1">
				&emsp; Pertengahan November diprediksi jadi titik curah hujan tertinggi di wilayah Banyumas. Fenomena alam ini pun memiliki potensi memicu bencana di beberapa wilayah Kabupaten Satria. “Kemungkinan pertengahan November sampai Februari 2022 itu curah hujan tertinggi,” kata Kepala Pelaksana BPBD Banyumas, Titik Pujiastuti melalui Sekretaris BPBD Banyumas, Gatot Epri. Menurutnya, seperti tahun-tahun yang lalu, curah hujan tinggi diikuti sejumlah ancaman bencana alam. Dua yang paling sering terjadi adalah banjir dan tanah longsor.
			</p>
			<p class="isi_artikel1">
				Gatot merinci, wilayah Banyumas secara garis besar biasa disapa tiga jenis bencana alam. Wilayah perbukitan seperti Pekuncen, Tambak, dan Kedungbanteng kerao disapa bencana tanah longsor. Sementara banjir kerap terjadi di beberapa kecamatan. Sebut saja, Kemranjen, Sumpiuh, Tambak, dan Rawalo. “Daerah Banyumas bagian barat memiliki potensi longsor. Sedangkan Banyumas bagian timur memiliki potensi banjir, tapi tergantung karakteristik lokasi juga,” ujar dia. Selanjutnya, ada bencana angin puting beliung di beberapa wilayah seperti Cilongok dan Pekuncen. Menghadapi ancama bencana ini, BPBD mengaku tengah menggalakkan patroli di beberapa wilayah. Ini dilakukan untuk meningkatkan kesiapsiagaan baik dari masyarakat maupun dari tim.(dec)
			</p>
